@TeleOp(name = "SensorTest")

public class SensorTest extends OpMode {
    AnalogInput ultrasonic;
    double voltage_to_in = 72.2891566265;

    @Override
    public void init(){
        ultrasonic = hardwareMap.get(AnalogInput.class, "ultrasonic");

    }

    @Override
    public void loop(){
        telemetry.addData("distance voltage: ", ultrasonic.getVoltage());
        telemetry.addData("distance inch: ", ultrasonic.getVoltage()*voltage_to_in);
        telemetry.update();
    }
}
